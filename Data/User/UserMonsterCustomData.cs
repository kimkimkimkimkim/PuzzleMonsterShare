/// <summary>
/// インベントリのモンスターデータのカスタムデータ
/// </summary>
public class UserMonsterCustomData
{
    /// <summary>
    /// レベル
    /// </summary>
    public int level { get; set; }

    /// <summary>
    /// 今まで取得した総経験値
    /// </summary>
    public int exp { get; set; }

    /// <summary>
    /// グレード
    /// </summary>
    public int grade { get; set; }

    /// <summary>
    /// 体力
    /// </summary>
    public int hp { get; set; }

    /// <summary>
    /// 攻撃力
    /// </summary>
    public int attack { get; set; }
}
